package handler;

public class Coordinate {

     int x_coordinate;
     int y_coordinate;

     public Coordinate(int x_coordinate, int y_coordinate){
          this.x_coordinate = x_coordinate;
          this.y_coordinate = y_coordinate;
     }

     public Coordinate(){

          this.x_coordinate = -1;
          this.y_coordinate = -1;

     }

}
